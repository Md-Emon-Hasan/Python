# String Slicing And Other Functions In Python
mystr = 'harry is a good boy'
print(mystr)

mystr = 'harry is a good boy'
print(mystr[4])

mystr = 'harry is a good boy'
print(mystr[0:4])
print(mystr[:19])

mystr = 'harry is a good boy'
print(len(mystr))

mystr = 'harry is a good boy'
print(mystr[0:5:2])

mystr = 'harry is a good boy'
print(mystr[:19:1])

mystr = 'harry is a good boy'
print(mystr[-4:-2])

mystr = 'harry is a good boy'
print(mystr[::-2])

mystr = 'harry is a good boy'
print(mystr.isalnum())
mystr = 'HarryIsaGoodBoy'
print(mystr.isalnum())

mystr = 'harry is a good boy'
print(mystr.isalpha())

mystr = 'harry is a good boy'
print(mystr.endswith('body'))
print(mystr.count('h'))

mystr = 'harry is a good boy'
print(mystr.count('o'))

mystr = 'harry is a good boy'
print(mystr.capitalize())

mystr = 'harry is a good boy'
print(mystr.find('is'))

mystr = 'harry is a good boy'
print(mystr.lower())

mystr = 'harry is a good boy'
print(mystr.upper())

mystr = 'harry is a good boy'
print(mystr.replace('is','are'))

